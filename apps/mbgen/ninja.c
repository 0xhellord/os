INT
MbgenCreateNinja (
    PMBGEN_CONTEXT Context
    )

/*++

Routine Description:

    This routine creates a Ninja build file out of the build graph.

Arguments:

    Context - Supplies a pointer to the application context.

Return Value:

    0 on success.

    Returns an error number on failure.

--*/

{

    PLIST_ENTRY CurrentEntry;
    FILE *File;
    UINTN Index;
    PMBGEN_TARGET Input;
    PSTR NinjaPath;
    PMBGEN_POOL Pool;
    PLIST_ENTRY PoolEntry;
    PMBGEN_SCRIPT Script;
    PLIST_ENTRY ScriptEntry;
    PMBGEN_SOURCE Source;
    INT Status;
    PMBGEN_TARGET Target;
    time_t Time;
    PMBGEN_TOOL Tool;

    File = NULL;
    NinjaPath = MbgenAppendPaths(Context->BuildRoot, MBGEN_NINJA_FILE);
    if (NinjaPath == NULL) {
        Status = ENOMEM;
        goto CreateNinjaEnd;
    }

    if ((Context->Options & MBGEN_OPTION_VERBOSE) != 0) {
        printf("Creating %s\n", NinjaPath);
    }

    File = fopen(NinjaPath, "w");
    if (File == NULL) {
        Status = errno;
        fprintf(stderr,
                "Error: Failed to open %s: %s\n",
                NinjaPath,
                strerror(Status));

        goto CreateNinjaEnd;
    }

    Time = time(NULL);
    fprintf(File,
            "# Ninja build automatically generated by mbgen at %s\n",
            ctime(&Time));

    fprintf(File, "# Define high level variables\n");
    fprintf(File,
            "%s = %s\n",
            MBGEN_VARIABLE_SOURCE_ROOT,
            Context->SourceRoot);

    fprintf(File,
            "%s = %s\n",
            MBGEN_VARIABLE_BUILD_ROOT,
            Context->BuildRoot);

    fprintf(File,
            "%s = %s\n",
            MBGEN_VARIABLE_PROJECT_PATH,
            Context->ProjectFilePath);

    MbgenNinjaPrintConfig(File, Context, NULL);
    fprintf(File, "\n# Define tools\n");
    CurrentEntry = Context->ToolList.Next;
    while (CurrentEntry != &(Context->ToolList)) {
        Tool = LIST_VALUE(CurrentEntry, MBGEN_TOOL, ListEntry);
        fprintf(File, "rule %s\n", Tool->Name);
        if (Tool->Description != NULL) {
            fprintf(File, "    description = ");
            MbgenNinjaPrintWithVariableConversion(File, Tool->Description);
            fprintf(File, "\n");
        }

        fprintf(File, "    command = ");
        MbgenNinjaPrintWithVariableConversion(File, Tool->Command);
        fprintf(File, "\n");
        if (Tool->Depfile != NULL) {
            fprintf(File, "    depfile = ");
            MbgenNinjaPrintWithVariableConversion(File, Tool->Depfile);
            fprintf(File, "\n");
        }

        if (Tool->DepsFormat != NULL) {
            fprintf(File, "    deps = %s\n", Tool->DepsFormat);
        }

        if (Tool->Pool != NULL) {
            fprintf(File, "    pool = %s\n", Tool->Pool);
        }

        fprintf(File, "\n");
        CurrentEntry = CurrentEntry->Next;
    }

    if (!LIST_EMPTY(&(Context->PoolList))) {
        fprintf(File, "# Define pools");
    }

    PoolEntry = Context->PoolList.Next;
    while (PoolEntry != &(Context->PoolList)) {
        Pool = LIST_VALUE(PoolEntry, MBGEN_POOL, ListEntry);
        fprintf(File, "\npool %s\n    depth = %d\n", Pool->Name, Pool->Depth);
        PoolEntry = PoolEntry->Next;
    }

    fprintf(File, "\n");

    //
    // Loop over every script (file) in the build.
    //

    ScriptEntry = Context->ScriptList.Next;
    while (ScriptEntry != &(Context->ScriptList)) {
        Script = LIST_VALUE(ScriptEntry, MBGEN_SCRIPT, ListEntry);
        ScriptEntry = ScriptEntry->Next;
        if (LIST_EMPTY(&(Script->TargetList))) {
            continue;
        }

        //
        // Loop over every target defined in the script.
        //

        if (Script->Path[0] == '\0') {
            fprintf(File, "# Define root targets\n");

        } else {
            fprintf(File, "# Define targets for %s\n", Script->Path);
        }

        CurrentEntry = Script->TargetList.Next;
        while (CurrentEntry != &(Script->TargetList)) {
            Target = LIST_VALUE(CurrentEntry, MBGEN_TARGET, ListEntry);
            CurrentEntry = CurrentEntry->Next;

            //
            // Add the configs for this target.
            //

            fprintf(File, "build ");
            MbgenNinjaPrintTargetFile(File, Context, Target);
            fprintf(File, ": %s ", Target->Tool);

            //
            // Add the inputs.
            //

            for (Index = 0; Index < Target->Inputs.Count; Index += 1) {
                Input = Target->Inputs.Array[Index];
                switch (Input->Type) {
                case MbgenInputTarget:
                    MbgenNinjaPrintTargetFile(File, Context, Input);
                    break;

                case MbgenInputSource:
                    Source = (PMBGEN_SOURCE)Input;
                    MbgenNinjaPrintSource(File, Context, Source);
                    break;

                default:

                    assert(FALSE);

                    break;
                }

                if (Index + 1 != Target->Inputs.Count) {
                    fprintf(File, MBGEN_NINJA_LINE_CONTINUATION "        ");
                }
            }

            //
            // Add the implicit inputs if there are any.
            //

            if (Target->Implicit.Count != 0) {
                fprintf(File, " | " MBGEN_NINJA_LINE_CONTINUATION);
                for (Index = 0;
                     Index < Target->Implicit.Count;
                     Index += 1) {

                    Input = Target->Implicit.Array[Index];
                    switch (Input->Type) {
                    case MbgenInputTarget:
                        MbgenNinjaPrintTargetFile(File, Context, Input);
                        break;

                    case MbgenInputSource:
                        Source = (PMBGEN_SOURCE)Input;
                        MbgenNinjaPrintSource(File, Context, Source);
                        break;

                    default:

                        assert(FALSE);

                        break;
                    }

                    if (Index + 1 != Target->Implicit.Count) {
                        fprintf(File, MBGEN_NINJA_LINE_CONTINUATION "        ");
                    }
                }
            }

            //
            // Add the order-only inputs if there are any.
            //

            if (Target->OrderOnly.Count != 0) {
                fprintf(File, " || " MBGEN_NINJA_LINE_CONTINUATION);
                for (Index = 0;
                     Index < Target->OrderOnly.Count;
                     Index += 1) {

                    Input = Target->OrderOnly.Array[Index];
                    switch (Input->Type) {
                    case MbgenInputTarget:
                        MbgenNinjaPrintTargetFile(File, Context, Input);
                        break;

                    case MbgenInputSource:
                        Source = (PMBGEN_SOURCE)Input;
                        MbgenNinjaPrintSource(File, Context, Source);
                        break;

                    default:

                        assert(FALSE);

                        break;
                    }

                    if (Index + 1 != Target->OrderOnly.Count) {
                        fprintf(File, MBGEN_NINJA_LINE_CONTINUATION "        ");
                    }
                }
            }

            fprintf(File, "\n");
            MbgenNinjaPrintConfig(File, Context, Target);
            if (Target->Pool != NULL) {
                fprintf(File, "    pool = %s\n", Target->Pool);
            }

            //
            // Separate targets with newlines, except squeeze together a bunch
            // of one-liners.
            //

            if ((Target->Inputs.Count > 1) ||
                (Target->Implicit.Count != 0) ||
                (Target->OrderOnly.Count != 0) ||
                ((Target->Config != NULL) &&
                 (!LIST_EMPTY(&(Target->Config->Dict.EntryList)))) ||
                (Target->Pool != NULL) ||
                (CurrentEntry == &(Script->TargetList))) {

                fprintf(File, "\n");
            }
        }
    }

    MbgenNinjaPrintRebuildRule(Context, File);
    Status = 0;

CreateNinjaEnd:
    if (File != NULL) {
        fclose(File);
    }

    if (NinjaPath != NULL) {
        free(NinjaPath);
    }

    return Status;
}

